from __future__ import annotations

import argparse
import csv
import json
import sys
import time
import urllib.parse
import xml.etree.ElementTree as ET
from dataclasses import dataclass
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path
from typing import Iterator

import requests


S3_ENDPOINT = "https://oedi-data-lake.s3.amazonaws.com/"
S3_NS = {"s": "http://s3.amazonaws.com/doc/2006-03-01/"}


@dataclass(frozen=True)
class DownloadDataset:
    dataset_id: str
    source: str
    prefix: str
    local_subdir: str
    description: str


DATASETS: dict[str, DownloadDataset] = {
    "smart_ds_aus_p1u_base_opendss": DownloadDataset(
        dataset_id="smart_ds_aus_p1u_base_opendss",
        source="oedi_s3",
        prefix="SMART-DS/v1.0/2018/AUS/P1U/scenarios/base_timeseries/opendss/",
        local_subdir="smart_ds/v1.0/2018/AUS/P1U/base_timeseries/opendss",
        description=(
            "NREL SMART-DS Austin P1U base-timeseries OpenDSS data. This is a real "
            "multi-feeder distribution dataset, not a synthetic toy profile generated by this project."
        ),
    ),
    "smart_ds_aus_p1u_profiles": DownloadDataset(
        dataset_id="smart_ds_aus_p1u_profiles",
        source="oedi_s3",
        prefix="SMART-DS/v1.0/2018/AUS/P1U/profiles/",
        local_subdir="smart_ds/v1.0/2018/AUS/P1U/profiles",
        description=(
            "NREL SMART-DS Austin P1U annual 15-minute profile CSV files referenced by "
            "OpenDSS LoadShapes. Required for real long-horizon load-driven training."
        ),
    ),
}


def _list_oedi_s3(prefix: str, session: requests.Session) -> list[dict[str, object]]:
    rows: list[dict[str, object]] = []
    token: str | None = None
    while True:
        params = {"list-type": "2", "prefix": prefix, "max-keys": "1000"}
        if token:
            params["continuation-token"] = token
        response = session.get(S3_ENDPOINT, params=params, timeout=60)
        response.raise_for_status()
        root = ET.fromstring(response.content)
        for item in root.findall("s:Contents", S3_NS):
            key = item.findtext("s:Key", namespaces=S3_NS)
            if not key:
                continue
            rows.append(
                {
                    "key": key,
                    "size_bytes": int(item.findtext("s:Size", default="0", namespaces=S3_NS)),
                    "last_modified": item.findtext("s:LastModified", default="", namespaces=S3_NS),
                    "etag": (item.findtext("s:ETag", default="", namespaces=S3_NS) or "").strip('"'),
                    "url": urllib.parse.urljoin(S3_ENDPOINT, urllib.parse.quote(key)),
                }
            )
        if root.findtext("s:IsTruncated", namespaces=S3_NS) != "true":
            break
        token = root.findtext("s:NextContinuationToken", namespaces=S3_NS)
        if not token:
            break
    return rows


def _iter_selected(rows: list[dict[str, object]], *, max_files: int | None) -> Iterator[dict[str, object]]:
    for index, row in enumerate(rows):
        if max_files is not None and index >= max_files:
            break
        yield row


def _download_file(
    *,
    row: dict[str, object],
    local_path: Path,
    force: bool,
) -> str:
    expected_size = int(row["size_bytes"])
    if local_path.exists() and not force and local_path.stat().st_size == expected_size:
        return "skipped_existing"
    local_path.parent.mkdir(parents=True, exist_ok=True)
    temp_path = local_path.with_suffix(local_path.suffix + ".part")
    response = requests.get(str(row["url"]), stream=True, timeout=120)
    response.raise_for_status()
    with temp_path.open("wb") as handle:
        for chunk in response.iter_content(chunk_size=1024 * 1024):
            if chunk:
                handle.write(chunk)
    if expected_size and temp_path.stat().st_size != expected_size:
        raise IOError(
            f"Downloaded size mismatch for {row['key']}: expected {expected_size}, got {temp_path.stat().st_size}."
        )
    temp_path.replace(local_path)
    return "downloaded"


def download_dataset(
    dataset: DownloadDataset,
    *,
    output_root: Path,
    dry_run: bool,
    force: bool,
    max_files: int | None,
    workers: int,
) -> dict[str, object]:
    session = requests.Session()
    rows = _list_oedi_s3(dataset.prefix, session)
    selected = list(_iter_selected(rows, max_files=max_files))
    total_bytes = int(sum(int(row["size_bytes"]) for row in selected))
    target_root = output_root / dataset.local_subdir
    manifest_rows: list[dict[str, object]] = []
    started = time.time()
    tasks: list[dict[str, object]] = []
    for row in selected:
        relative = str(row["key"])[len(dataset.prefix) :].lstrip("/")
        local_path = target_root / relative
        tasks.append({"row": row, "local_path": local_path})

    if dry_run:
        for task in tasks:
            row = task["row"]
            manifest_rows.append(
                {
                    "dataset_id": dataset.dataset_id,
                    "source_key": row["key"],
                    "url": row["url"],
                    "size_bytes": row["size_bytes"],
                    "local_path": str(task["local_path"]),
                    "status": "dry_run",
                }
            )
    else:
        completed = 0
        with ThreadPoolExecutor(max_workers=max(1, int(workers))) as executor:
            future_map = {
                executor.submit(
                    _download_file,
                    row=task["row"],
                    local_path=task["local_path"],
                    force=force,
                ): task
                for task in tasks
            }
            for future in as_completed(future_map):
                task = future_map[future]
                row = task["row"]
                try:
                    status = future.result()
                except Exception as exc:
                    status = f"failed:{type(exc).__name__}:{exc}"
                completed += 1
                manifest_rows.append(
                    {
                        "dataset_id": dataset.dataset_id,
                        "source_key": row["key"],
                        "url": row["url"],
                        "size_bytes": row["size_bytes"],
                        "local_path": str(task["local_path"]),
                        "status": status,
                    }
                )
                if completed % 100 == 0 or completed == len(tasks):
                    processed_bytes = sum(int(item["size_bytes"]) for item in manifest_rows)
                    print(
                        f"[{dataset.dataset_id}] processed {completed}/{len(tasks)} files "
                        f"({processed_bytes / 2**20:.1f} MiB)",
                        flush=True,
                    )
    output_root.mkdir(parents=True, exist_ok=True)
    suffix = "_dry_run" if dry_run else ""
    manifest_path = output_root / f"{dataset.dataset_id}_download_manifest{suffix}.csv"
    with manifest_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(
            handle,
            fieldnames=["dataset_id", "source_key", "url", "size_bytes", "local_path", "status"],
        )
        writer.writeheader()
        writer.writerows(manifest_rows)
    summary = {
        "dataset_id": dataset.dataset_id,
        "description": dataset.description,
        "source": dataset.source,
        "prefix": dataset.prefix,
        "target_root": str(target_root),
        "manifest": str(manifest_path),
        "dry_run": bool(dry_run),
        "file_count": int(len(selected)),
        "total_size_bytes": total_bytes,
        "total_size_mib": round(total_bytes / 2**20, 3),
        "downloaded_files": int(sum(1 for row in manifest_rows if row["status"] == "downloaded")),
        "skipped_existing_files": int(sum(1 for row in manifest_rows if row["status"] == "skipped_existing")),
        "failed_files": int(sum(1 for row in manifest_rows if str(row["status"]).startswith("failed:"))),
        "workers": int(workers),
        "elapsed_seconds": round(time.time() - started, 3),
    }
    summary_path = output_root / f"{dataset.dataset_id}_download_summary{suffix}.json"
    summary_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")
    return summary


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Download open datasets for real DSO-VPP training.")
    parser.add_argument(
        "--dataset",
        choices=sorted(DATASETS),
        default="smart_ds_aus_p1u_base_opendss",
        help="Dataset preset to download.",
    )
    parser.add_argument("--output-root", default="data/external/raw", help="Directory for downloaded data.")
    parser.add_argument("--dry-run", action="store_true", help="Only list files and write a manifest.")
    parser.add_argument("--force", action="store_true", help="Redownload files even if size already matches.")
    parser.add_argument("--max-files", type=int, default=None, help="Optional debug limit.")
    parser.add_argument("--workers", type=int, default=8, help="Concurrent download workers.")
    args = parser.parse_args(argv)

    dataset = DATASETS[args.dataset]
    summary = download_dataset(
        dataset,
        output_root=Path(args.output_root),
        dry_run=bool(args.dry_run),
        force=bool(args.force),
        max_files=args.max_files,
        workers=int(args.workers),
    )
    print(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
